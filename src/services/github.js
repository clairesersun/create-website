export async function validatePat(pat) {
  const response = await fetch('https://api.github.com/user', {
    headers: {
      Authorization: `Bearer ${pat}`,
      Accept: 'application/vnd.github.v3+json',
    },
  });

  if (!response.ok) {
    throw new Error('Invalid GitHub PAT');
  }

  const user = await response.json();
  return user.login;
}

export async function createRepo(pat, repoName) {
  const response = await fetch('https://api.github.com/user/repos', {
    method: 'POST',
    headers: {
      Authorization: `Bearer ${pat}`,
      Accept: 'application/vnd.github.v3+json',
      'Content-Type': 'application/json',
    },
    body: JSON.stringify({
      name: repoName,
      description: 'Website generated by Web Prospector',
      public: true,
      auto_init: false,
    }),
  });

  if (!response.ok) {
    const err = await response.json();
    if (err.errors?.some((e) => e.message?.includes('name already exists'))) {
      throw new Error('REPO_EXISTS');
    }
    throw new Error(`Failed to create repo: ${err.message}`);
  }

  return await response.json();
}

export async function uploadFile(pat, owner, repo, filePath, content, message) {
  const encoded = btoa(unescape(encodeURIComponent(content)));

  const response = await fetch(
    `https://api.github.com/repos/${owner}/${repo}/contents/${filePath}`,
    {
      method: 'PUT',
      headers: {
        Authorization: `Bearer ${pat}`,
        Accept: 'application/vnd.github.v3+json',
        'Content-Type': 'application/json',
      },
      body: JSON.stringify({
        message,
        content: encoded,
        branch: 'main',
      }),
    }
  );

  if (!response.ok) {
    const err = await response.json();
    throw new Error(`Failed to upload file: ${err.message}`);
  }

  return await response.json();
}

