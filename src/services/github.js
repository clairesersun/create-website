export async function getPagesStatus(pat, owner, repo) {
  const response = await fetch(
    `https://api.github.com/repos/${owner}/${repo}/pages`,
    {
      headers: {
        Authorization: `Bearer ${pat}`,
        Accept: 'application/vnd.github.v3+json',
      },
    }
  );

  if (!response.ok) {
    return null;
  }

  return await response.json();
}

export async function waitForPages(pat, owner, repo, maxAttempts = 30) {
  for (let i = 0; i < maxAttempts; i++) {
    const status = await getPagesStatus(pat, owner, repo);
    if (status?.status === 'built' && status?.html_url) {
      return status.html_url;
    }
    await new Promise((resolve) => setTimeout(resolve, 3000));
  }
  // Return expected URL even if polling timed out
  return `https://${owner}.github.io/${repo}`;
}

